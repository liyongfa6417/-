/*
*******************************************************************************
**函    数:Can_Send_Msg
**功    能:用于CAN数据的发送,can发送一组数据(固定格式:ID为0X12,标准帧,数据帧)	
**参    数:len:数据长度(最大为8);msg:数据指针,最大为8个字节.	
**返 回 值:0,成功;其他,失败;
**创建日期:
**修改记录:
**说    明:
*******************************************************************************
*/
u8 Can_Send_Msg(u8* msg,u8 len,u32 address_ID)
{	
  u8 mbox;
  u16 i=0;
  CanTxMsg TxMessage;
  TxMessage.StdId=address_ID;					 // 标准标识符为0
  TxMessage.ExtId=address_ID;		 // 设置扩展标示符（29位）
  TxMessage.IDE=CAN_ID_STD;			 //  使用标准标识符
  TxMessage.RTR=CAN_RTR_DATA;		 // 消息类型为数据帧，一帧8位
  TxMessage.DLC=len;						 // 发送两帧信息
	
  for(i=0;i<len;i++)
  TxMessage.Data[i]=msg[i];			 // 第一帧信息  
	
  mbox= CAN_Transmit(CAN1, &TxMessage);   
  i=0;
  while((CAN_TransmitStatus(CAN1, mbox)==CAN_TxStatus_Failed)&&(i<0XFFF))
		i++;	//等待发送结束
  if(i>=0XFFF)return 1;
  return 0;		

}
/*
*******************************************************************************
**函    数:Can_Receive_Msg
**功    能:can口接收数据查询
**参    数:数据缓存区;	 
**返 回 值:0,无数据被收到;其他,接收的数据长度;
**创建日期:
**修改记录:
**说    明:
*******************************************************************************
*/
u8 Can_Receive_Msg(u8 *buf)
{		   		   
 	 u32 i;
	 CanRxMsg RxMessage;
   if( CAN_MessagePending(CAN1,CAN_FIFO0)==0)return 0;		//没有接收到数据,直接退出 
   CAN_Receive(CAN1, CAN_FIFO0, &RxMessage);              //读取数据	
   for(i=0;i<8;i++)
   buf[i]=RxMessage.Data[i];  
	 return RxMessage.DLC;	
}


